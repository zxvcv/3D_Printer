Std_Err init_U11(SystemCommand_Settings* settings, SystemCommand* cmd)
{
    Std_Err stdErr;

    stdErr = forward_command_immediately(settings, cmd, "G91", false);
    if(stdErr != STD_OK) { return stdErr; }

    stdErr = forward_command_concurrently(settings, cmd, "G1", true);

    return stdErr;
}
