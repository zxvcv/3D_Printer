Std_Err init_U40(SystemCommand* cmd)
{
    cmd->remove = NULL;
    cmd->step = NULL;

    f_mount(settings->fatfs, "", 0);
    f_open(settings->sd->file, "fl.txt", FA_READ);

    settings->sd->executing_SDprogram = true;
    return STD_OK;
}
