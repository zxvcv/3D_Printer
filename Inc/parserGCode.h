#include <stdbool.h>
#include "a4988_stepstick.h"
#include "manager.h"



/* #######################################################################################################
 *											DEFINES
 * ####################################################################################################### */

#define GCODE_COMMANDS_NUM 10



/* #######################################################################################################
 *											DATA TYPES
 * ####################################################################################################### */

typedef enum GCode_Err_Tag{
	GCODE_OK,
	GCODE_ERROR
} GCode_Err;

typedef struct GCodeCommand_Tag{
	GCode_Err (*execute)(struct GCodeCommand_Tag*, DeviceSettings*) ;	//command pointer
	int cmdNum;

	double x;				//X-axis move
	double y;				//Y-axis move
	double z;				//Z-axis move
	double e;				//extruder-axis move
	double f;				//speed of the movement
	double s;				//temperature

	struct UsedFields{
		unsigned short _x : 1;
		unsigned short _y : 1;
		unsigned short _z : 1;
		unsigned short _e : 1;
		unsigned short _f : 1;
		unsigned short _s : 1;
	}usedFields;
}GCodeCommand;



